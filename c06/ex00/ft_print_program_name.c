#include <unistd.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(int argc, char **argv)
{
	argc = ft_strlen(argv[0]);
	write(1, argv[0], argc);
	write(1, "\n", 1);
	return 0;
}